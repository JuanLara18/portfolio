#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const BLOG_DATA = path.join(__dirname, '..', 'src', 'data', 'blogData.json');
const KB_DIR = path.join(__dirname, '..', '..', 'knowledge-base');
const KB_MD = path.join(KB_DIR, 'KNOWLEDGE_BASE.md');
const KB_JSON = path.join(KB_DIR, 'posts.json');

const SITE_BASE = 'https://juanlara18.github.io/portfolio';

function depthFromWordCount(wc) {
  if (!wc || wc < 1500) return 'intro';
  if (wc < 4000) return 'working';
  return 'deep';
}

function extractAugmentationYaml(md) {
  const re = /##\s+Augmentation[\s\S]*?```yaml\s*\n([\s\S]*?)\n```/;
  const m = md.match(re);
  if (!m) return {};
  try {
    const parsed = yaml.load(m[1]);
    return parsed && typeof parsed === 'object' ? parsed : {};
  } catch (err) {
    console.warn('Could not parse augmentation YAML:', err.message);
    return {};
  }
}

function normalizeList(v) {
  if (!v) return [];
  if (Array.isArray(v)) return v.map(x => String(x).trim()).filter(Boolean);
  return [String(v).trim()].filter(Boolean);
}

function buildPostEntry(post, augment) {
  const aug = augment[post.slug] || {};
  const tagsLower = (post.tags || []).map(t => String(t).toLowerCase());
  const concepts = aug.concepts ? normalizeList(aug.concepts) : tagsLower;
  const prereqs = normalizeList(aug.prereqs);
  const teaches = normalizeList(aug.teaches);
  const tech = normalizeList(aug.tech);
  const depth = aug.depth || depthFromWordCount(post.estimatedWordCount);
  const audioLangs = post.audio ? Object.keys(post.audio) : [];

  return {
    title: post.title,
    date: post.date,
    category: post.category,
    tags: post.tags || [],
    excerpt: post.excerpt,
    url: `${SITE_BASE}/#/blog/${post.slug}`,
    source_path: `front/public/blog/posts/${post.category}/${post.slug}.md`,
    reading_minutes: post.readingTimeMinutes || post.readingTime || null,
    word_count: post.estimatedWordCount || null,
    concepts,
    prereqs,
    teaches,
    tech,
    depth,
    audio_available: audioLangs,
    augmented: Boolean(augment[post.slug]),
  };
}

function buildIndexes(posts) {
  const concept = {};
  const tech = {};
  const tag = {};
  const category = {};
  const edges = [];

  for (const [slug, p] of Object.entries(posts)) {
    for (const c of p.concepts) {
      const key = String(c).toLowerCase();
      if (!concept[key]) concept[key] = [];
      concept[key].push({ slug, depth: p.depth });
    }
    for (const t of p.tech) {
      const key = String(t).toLowerCase();
      if (!tech[key]) tech[key] = [];
      tech[key].push(slug);
    }
    for (const t of p.tags || []) {
      if (!tag[t]) tag[t] = [];
      tag[t].push(slug);
    }
    if (!category[p.category]) category[p.category] = [];
    category[p.category].push(slug);
    for (const pre of p.prereqs) {
      if (posts[pre]) {
        edges.push({ from: pre, to: slug });
      }
    }
  }

  for (const key of Object.keys(concept)) concept[key].sort((a, b) => a.slug.localeCompare(b.slug));
  for (const key of Object.keys(tech)) tech[key].sort();
  for (const key of Object.keys(tag)) tag[key].sort();
  for (const key of Object.keys(category)) category[key].sort();
  edges.sort((a, b) => a.from.localeCompare(b.from) || a.to.localeCompare(b.to));

  return { concept_index: concept, tech_index: tech, tag_index: tag, category_index: category, prereq_graph: { edges } };
}

const CATALOG_START = '<!-- AUTO-CATALOG:START - regenerated by build-knowledge-base.js, do not edit by hand -->';
const CATALOG_END = '<!-- AUTO-CATALOG:END -->';

function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function buildCatalog(posts) {
  const byCategory = {};
  for (const [slug, p] of Object.entries(posts)) {
    if (!byCategory[p.category]) byCategory[p.category] = [];
    byCategory[p.category].push({ slug, ...p });
  }
  const order = ['field-notes', 'research', 'curiosities'];
  const cats = [
    ...order.filter(c => byCategory[c]),
    ...Object.keys(byCategory).filter(c => !order.includes(c)).sort(),
  ];

  const lines = [];
  lines.push('## Full Post Catalog');
  lines.push('');
  lines.push('Auto-generated index of every post by category, sorted most recent first. Use this when you need a complete inventory of what the blog covers — for example, when loaded as Claude Project knowledge and you cannot query `posts.json`.');
  lines.push('');

  for (const cat of cats) {
    const items = byCategory[cat].sort((a, b) => (b.date || '').localeCompare(a.date || ''));
    lines.push(`### ${cat} (${items.length} post${items.length === 1 ? '' : 's'})`);
    lines.push('');
    for (const p of items) {
      const concepts = p.concepts && p.concepts.length ? ` Concepts: ${p.concepts.slice(0, 6).join(', ')}.` : '';
      const tech = p.tech && p.tech.length ? ` Tech: ${p.tech.join(', ')}.` : '';
      const excerpt = (p.excerpt || '').replace(/\s+/g, ' ').trim();
      lines.push(`- **\`${p.slug}\`** *(${p.depth})* — ${p.title}. ${excerpt}${concepts}${tech}`);
    }
    lines.push('');
  }
  return lines.join('\n').trim();
}

function injectCatalog(md, catalog) {
  const block = `${CATALOG_START}\n\n${catalog}\n\n${CATALOG_END}`;
  const re = new RegExp(`${escapeRegExp(CATALOG_START)}[\\s\\S]*?${escapeRegExp(CATALOG_END)}`);
  if (re.test(md)) return md.replace(re, block);
  return md.trimEnd() + '\n\n' + block + '\n';
}

function buildKnowledgeBase() {
  console.log('Building knowledge base...');

  if (!fs.existsSync(BLOG_DATA)) {
    console.error('blogData.json not found. Run build-blog-data first.');
    process.exit(1);
  }
  if (!fs.existsSync(KB_MD)) {
    console.error('KNOWLEDGE_BASE.md not found at', KB_MD);
    process.exit(1);
  }

  const blogData = JSON.parse(fs.readFileSync(BLOG_DATA, 'utf8'));
  const kbMd = fs.readFileSync(KB_MD, 'utf8');
  const augment = extractAugmentationYaml(kbMd);

  const posts = {};
  for (const p of blogData.posts || []) {
    if (!p.slug) continue;
    posts[p.slug] = buildPostEntry(p, augment);
  }

  const indexes = buildIndexes(posts);

  const augmentedCount = Object.values(posts).filter(p => p.augmented).length;
  const unknownAugKeys = Object.keys(augment).filter(s => !posts[s]);
  if (unknownAugKeys.length) {
    console.warn('Augmentation entries with no matching post:', unknownAugKeys.join(', '));
  }

  const output = {
    $schema_version: '1.0',
    generated_at: new Date().toISOString(),
    manifest: {
      purpose: 'Machine-readable index of the blog. Pair with knowledge-base/KNOWLEDGE_BASE.md for narrative context, reading paths, and cross-cutting views.',
      authoring_flow: 'Edit knowledge-base/KNOWLEDGE_BASE.md (the curated source). Run `npm run build-knowledge-base` (or any `npm run build`) to regenerate this file.',
      how_to_open_full_post: 'Read the file at the `source_path` field. Slugs are stable canonical IDs.',
      defaults_when_not_augmented: 'concepts = lowercased tags; prereqs = []; teaches = []; tech = []; depth = inferred from word_count (<1500 intro, <4000 working, else deep).',
    },
    stats: {
      total_posts: Object.keys(posts).length,
      augmented_posts: augmentedCount,
      by_category: Object.fromEntries(
        Object.entries(indexes.category_index).map(([k, v]) => [k, v.length])
      ),
      concepts_indexed: Object.keys(indexes.concept_index).length,
      tech_indexed: Object.keys(indexes.tech_index).length,
      prereq_edges: indexes.prereq_graph.edges.length,
    },
    posts,
    ...indexes,
  };

  fs.writeFileSync(KB_JSON, JSON.stringify(output, null, 2));
  console.log(`[OK] posts.json written: ${output.stats.total_posts} post(s), ${augmentedCount} augmented, ${output.stats.concepts_indexed} concept(s) -> ${path.relative(process.cwd(), KB_JSON)}`);

  const catalog = buildCatalog(posts);
  const newMd = injectCatalog(kbMd, catalog);
  if (newMd !== kbMd) {
    fs.writeFileSync(KB_MD, newMd);
    console.log(`[OK] Catalog injected into ${path.relative(process.cwd(), KB_MD)}`);
  }
}

if (require.main === module) {
  buildKnowledgeBase();
}

module.exports = { buildKnowledgeBase };
